package enumeracion;
//Una eumeracion es un tipo similar a una clase, pero es una clase particuar que
//contiene varios elementos y por ello se le conoce como enumeracion, ya que va a 
//contener varios elementos definidos dentro de esta enumeracion.
//La diferencia entre una clase y una enumeracion, es que en lugar de utilizar la 
//palabra reservada class, vamos a utilizar la palabre reservada enum.
//Lo interesante de los elementos de la eumeracion, es que por default son elementos publicos,
//estaticos y finales. Una constante es publica, estatica y final.
public enum Dias {
    DOMINGO,
    LUNES, 
    MARTES,
    MIERCOLES,
    JUEVES,
    VIERNES,
    SABADO,
}
